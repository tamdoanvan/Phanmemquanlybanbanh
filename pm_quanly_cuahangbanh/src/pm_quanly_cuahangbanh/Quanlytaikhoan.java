    public void DS_taikhoan(){
        DefaultTableModel danhsachtaikhoan = (DefaultTableModel)tb_danhsachtaikhoan.getModel();
        try{
            Connect a = new Connect();
            Connection conn = a.getConnection();
            if (conn!=null){
                Statement dd = conn.createStatement();            
                 ResultSet  kq = dd.executeQuery("Select tennhanvien, idnhanvien,tinhtrang,chucvu from taikhoan");
                 while(kq.next()){
                    String tennhanvien = kq.getString(1);
                    String idnhanvien =kq.getString(2);
                    String tinhtrang = kq.getString(3);
                    String chucvu = kq.getString(4);
                    danhsachtaikhoan.addRow(new Object[]{tennhanvien, idnhanvien,chucvu,tinhtrang});
                }
                
            }
                      
        }catch(Exception ex){
            System.out.print(ex.toString());
        }
    }
    public boolean loi_kytu_tennhanvien (String chuoi){
        boolean kt =false;
        if(chuoi.length()>50){
            kt = false;
            return kt;
        }else{
            kt=true;
            return kt;
        }
    }
    public boolean loi_kytu (String chuoi){
        boolean kt =false;
        if(chuoi.length()>5||chuoi.length()<5){
            kt = false;
            return kt;
        }else{
            kt=true;
            return kt;
        }
    }
    public boolean loi_trungmaID (String chuoi){
        boolean kt =false;
        String maID="";
        try{
            Connect a = new Connect();
            Connection conn = a.getConnection();
            if (conn!=null){
                Statement dd = conn.createStatement();            
                 ResultSet  kq = dd.executeQuery("Select idnhanvien from taikhoan where idnhanvien = "+chuoi+"");
                 while(kq.next()){
                     maID = kq.getString(1);
                }
                
            }
                      
        }catch(Exception ex){
            System.out.print(ex.toString());
        }
        if(maID.equals("")){
            kt = true;
            return kt;
            
        }else{
            kt=false;
            return kt;
        }
    }
