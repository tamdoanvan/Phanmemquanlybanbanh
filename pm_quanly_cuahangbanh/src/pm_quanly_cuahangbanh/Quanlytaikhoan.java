    private void btn_chinhsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_chinhsuaActionPerformed
        // TODO add your handling code here:
        int Row = tb_danhsachtaikhoan.getSelectedRow();
        if(Row<0){
            JOptionPane.showMessageDialog(null,"Chưa chọn tài khoản trong danh sách!","Thông báo",JOptionPane.ERROR_MESSAGE);
        }else{
        String tennhanvien= txt_tennhanvien.getText();
        String idnhanvien= txt_idnhanvien.getText();
        String matkhau= txt_matkhau.getText();
        String chucvu =cb_chucvu.getSelectedItem().toString();
        
        if(tennhanvien.equals("")){
            JOptionPane.showMessageDialog(null,"Chưa nhập tên nhân viên!","Thông báo",JOptionPane.ERROR_MESSAGE);
        }else if(loi_kytu_tennhanvien (tennhanvien)==false){
            JOptionPane.showMessageDialog(null,"Nhập tên nhân viên không quá 50 ký tự!","Thông báo",JOptionPane.ERROR_MESSAGE);
        }else if(idnhanvien.equals("")){
            JOptionPane.showMessageDialog(null,"Chưa nhập ID nhân viên!","Thông báo",JOptionPane.ERROR_MESSAGE);
        }else if(loi_kytu(idnhanvien)==false){
            JOptionPane.showMessageDialog(null,"Nhập ID nhân viên 5 ký tự!","Thông báo",JOptionPane.ERROR_MESSAGE);
        }else if(matkhau.equals("")){
            JOptionPane.showMessageDialog(null,"Chưa nhập mật khẩu!","Thông báo",JOptionPane.ERROR_MESSAGE);
        }else if(loi_kytu(matkhau)==false){
            JOptionPane.showMessageDialog(null,"Nhập mật khẩu 5 ký tự!","Thông báo",JOptionPane.ERROR_MESSAGE);
        }else if(chucvu.equals("--Chọn--")){
            JOptionPane.showMessageDialog(null,"Chưa chọn chức vụ!","Thông báo",JOptionPane.ERROR_MESSAGE);
        }else{
            try{
            Connect a = new Connect();
            Connection conn = a.getConnection();
            
            if (JOptionPane.showConfirmDialog(this, "Chỉnh sửa tài khoản?", "Xác nhận", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            if (conn!=null){
                Statement dd = conn.createStatement();            
                dd.executeUpdate("Update taikhoan set tennhanvien = N'"+tennhanvien+"',idnhanvien = '"+idnhanvien+"', matkhau = "+matkhau+",chucvu = N'"+chucvu+"', tinhtrang = N'Not Active' where idnhanvien = '"+idnhanvien+"'     ");
                JOptionPane.showMessageDialog(null,"Chỉnh sửa tài khoản thành công!","Thông báo",JOptionPane.INFORMATION_MESSAGE);
                DefaultTableModel danhsachtaikhoan = (DefaultTableModel)tb_danhsachtaikhoan.getModel();
                danhsachtaikhoan.setRowCount(0);
                txt_tennhanvien.setText("");
                txt_idnhanvien.setText("");
                txt_matkhau.setText("");
                cb_chucvu.setSelectedItem("--Chọn--");
                DS_taikhoan();
            }
            }     
        }catch(Exception ex){
            System.out.print(ex);
            JOptionPane.showMessageDialog(null,"Chỉnh sửa không thành công!","Thông báo",JOptionPane.ERROR_MESSAGE);
        }
        }
        }
    }//GEN-LAST:event_btn_chinhsuaActionPerformed

